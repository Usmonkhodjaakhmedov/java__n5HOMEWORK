let son = +prompt("sonni kriting")
let son1 = +prompt("sonni kriting")
let amaliy = prompt("amaliyni kriting")

if (amaliy == '-') {
    alert(son - son1)
}

else if (amaliy == '+') {
    alert(son + son1)
}

else if (amaliy == '*') {
    alert(son * son1)
}

else if (amaliy == '%') {
    alert(son % son1)
}